//******************************************************************************
// Perform the search.

double DelayStub::search()
{
   mXDelay = BaseClass::search(
      mXAinitial,
      mXBinitial,
      mTolerance,
      mMaxIterations);

   return mXDelay;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Perform the search.
double DelayStub::function(double aX)
{
   return pow(aX-2,2) + 1;
}


