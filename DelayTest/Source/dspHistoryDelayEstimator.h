   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Constructor and initialization.

   // Constructor.
   typedef Dsp::GoldSearchBase BaseClass;
   HistoryDelayEstimator();
   void reset();

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Write to the history.

   // Perform the search.
   double search(
      History* aHistory1,
      History* aHistory2,
      double   aSearchFs,
      double   aSearchDelay,
      double   aSearchTolerance,
      int      aSearchMaxIterations);

   // f(x). Error function.
   double function(double aX) override;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // More.

