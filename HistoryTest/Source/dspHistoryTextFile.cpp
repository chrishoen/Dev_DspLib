// Write a history to a csv file.

void HistoryCsvFileWriter::writeHistory(History& aHistory)
{
   // Loop through all of the samples in the history.
   for (int k = 0; k < aHistory.mMaxSamples; k++)
   {
      // Write the sample to the csv file
      writeRow(
         k,
         aHistory.mTime[k],
         aHistory.mValue[k]);
   }

}

