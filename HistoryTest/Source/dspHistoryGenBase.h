#ifndef _DSPHISTORYGENBASE_H_
#define _DSPHISTORYGENBASE_H_

/*==============================================================================
==============================================================================*/

//******************************************************************************
//******************************************************************************
//******************************************************************************

#include "dspHistory.h"

namespace Dsp
{

//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
//******************************************************************************
// This is a base class for signal history generators.

class HistoryGenBase
{
public:

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Input parameters.

   double  mDuration;     // Time duration of signal
   double  mFs;           // Sampling frequency

   double  mEX;           // Desired expectation
   double  mUX;           // Desired uncertainty

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Extra parameters.

   double  mTs;           // Sampling period
   int     mNumSamples;   // Number of samples in array

   //******************************************************************************
   //******************************************************************************
   //******************************************************************************
   // Constructor and initialization.
   // Create an new HistoryGenBase, set some of the members, call initialize to 
   // set other members.

   HistoryGenBase();
   virtual void reset();

   //--------------------------------------------------------------------------
   // Initialize variables. Initialize the history. Initialize the history time
   // array.
   virtual void initialize(History& aHistory);

   //--------------------------------------------------------------------------
   // Generate the signal history.

   virtual void generate(History& aHistory)=0;

   //--------------------------------------------------------------------------
   // Normalize the signal history.

   void normalize(History& aHistory);

   //--------------------------------------------------------------------------
   // Support.

   virtual void show();
};

//******************************************************************************
}//namespace

#endif

