double calcAngleBetween(ThreeVector& aV1, ThreeVector& aV2)
{
   double tL1 = aV1.length();
   double tL2 = aV2.length();

   if (tL1==0.0) return 0.0;
   if (tL2==0.0) return 0.0;

   return dsp_acos_deg((aV1*aV2)/(tL1*tL2));
}

