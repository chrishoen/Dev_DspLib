//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor

TimeSeriesFilteredGN::TimeSeriesFilteredGN()
{
   reset();
}

void TimeSeriesFilteredGN::reset()
{
   BaseClass::reset();
   mBSize=0;
   mASize=0;
   mBArray=0;
   mAArray=0;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize

void TimeSeriesFilteredGN::initialize()
{
   BaseClass::initialize();

   // Initialize bandpass filter
   mFilter.initialize(mBSize,mASize,mBArray,mAArray);
}
   
//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show

void TimeSeriesFilteredGN::show()
{
   BaseClass::show();
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void TimeSeriesFilteredGN::generate()
{
   //---------------------------------------------------------------------------
   // Initialize

   initialize();

   //---------------------------------------------------------------------------
   // Generate filtered guassian noise, allow filter to settle.

   for (int k = 0; k < 200; k++)
   {
      // Get noise.
      double tX = getNoise();

      // Filter the noise.
      mFilter.put(tX);
   }

   //---------------------------------------------------------------------------
   // Generate filtered guassian noise.

   for (int k = 0; k < mNumSamples; k++)
   {
      // Get noise.
      double tX = getNoise();

      // Filter the noise.
      mFilter.put(tX);

      // Store
      mX[k] = mFilter.mY;
   }

   //---------------------------------------------------------------------------
   // Normalize to get the desired expectation and uncertainty.

   normalize();
}

