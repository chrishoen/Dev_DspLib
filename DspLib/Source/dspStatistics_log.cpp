void TrialStatistics::log(int aLogNum,char* aLabel)
{
   Log::write(aLogNum,"%-10s %6llu $$ %12.6f %12.6f %12.6f  %12.6f",
      aLabel,
      mCount,
      mEX,
      mUX,
      mMinX,
      mMaxX);
}

