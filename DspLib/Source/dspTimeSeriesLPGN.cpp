// Constructor

TimeSeriesLPGN::TimeSeriesLPGN()
{
   reset();
}

void TimeSeriesLPGN::reset()
{
   BaseClass::reset();
   mN = 2;
   mFc = 1.0; 
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize

void TimeSeriesLPGN::initialize()
{
   BaseClass::initialize();

   mFilter.initialize(mN,mFs,mFc);
}
   
//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show

void TimeSeriesLPGN::show()
{
   BaseClass::show();
   printf("mN           %10d\n",  mN);
   printf("mFc          %10.4f\n",mFc);
   mFilter.show();
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void TimeSeriesLPGN::generate()
{
   //---------------------------------------------------------------------------
   // Generate filtered gaussian noise, allow filter to settle.

   for (int k = 0; k < 1000; k++)
   {
      // Get noise.
      double tX = getNoise();

      // Filter the noise.
      mFilter.put(tX);
   }

   //---------------------------------------------------------------------------
   // Generate filtered gaussian noise.
   // The filter is is a low pass butterworth filter.

   for (int k = 0; k < mNumSamples; k++)
   {
      // Get noise.
      double tX = getNoise();

      // Low pass filter the noise.
      mFilter.put(tX);
      mX[k] = mFilter.mY;
   }

   //---------------------------------------------------------------------------
   // Normalize to get the desired expectation and uncertainty.

   normalize();
}

