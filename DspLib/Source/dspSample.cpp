// Convert and store

Sample::Sample()
{
   mTime = 0.0;
   mAmplitude = 0.0;
}

void Sample::put(double aTime, double aVolts)
{
   mTime = aTime;
   mVolts = aVolts;
   mAmplitude = fabs(aVolts);
}
   
