ThreeAxisAngle::ThreeAxisAngle()
{
   mAxisX = 0.0;
   mAxisY = 0.0;
   mAxisZ = 0.0;
   mAngle = 0.0;
}

ThreeAxisAngle::ThreeAxisAngle(double aAxisX,double aAxisY,double aAxisZ,double aAngle)
{
   mAxisX = aAxisX;
   mAxisX = aAxisY;
   mAxisX = aAxisZ;
   mAngle = aAngle;
}

void ThreeAxisAngle::set(double aAxisX,double aAxisY,double aAxisZ,double aAngle)
{
   mAxisX = aAxisX;
   mAxisX = aAxisY;
   mAxisX = aAxisZ;
   mAngle = aAngle;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show

void ThreeAxisAngle::show(char* aLabel)
{
   printf("%10s ", aLabel);
   printf("%10.6f %10.6f %10.6f %10.6f", mAxisX, mAxisY, mAxisZ, mAngle);
   printf("\n");
}
