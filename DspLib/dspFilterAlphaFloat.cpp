void AlphaOneFloat::initialize(float aLambda)
{
   // Calculate filter parameters.
   float L  = aLambda;
   float L2 = L*L;
   float A = (-L2 + L*sqrtf(16 + L2))/8;
   mAlpha = A;

   // Initialize filter variables.
   mY   = 0.0;
   mXX  = 0.0;
   mFirstFlag = true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void AlphaOneFloat::initializeFromAlpha(float aAlpha)
{
   mAlpha = aAlpha;
   mY   = 0.0;
   mXX  = 0.0;
   mFirstFlag = true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void AlphaOneFloat::initializeFromLambda(float aLambda)
{
   initialize(aLambda);
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Initialize with time constants.

void AlphaOneFloat::initializeFromTc(float aTs, float aTc)
{
   float tNumer = (float)cDsp_TwoPi * (aTs / aTc);
   float tDenom = (float)cDsp_TwoPi * (aTs / aTc) + 1;
   mAlpha = tNumer / tDenom;

   mY = 0.0;
   mXX = 0.0;
   mFirstFlag = true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void AlphaOneFloat::setFirst()
{
   mY   = 0.0;
   mXX  = 0.0;
   mFirstFlag = true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

float AlphaOneFloat::put(float aY)
{
   if (mFirstFlag)
   {
      mFirstFlag = false;
      mXX = aY;
   }

   mY  = aY;

   float a = mAlpha;

   mXX = (1-a)*mXX + a*mY;

   return mXX;
}

float AlphaOneFloat::put(bool aCondition)
{
   if (aCondition) return put(1.0f);
   else            return put(0.0f);
}

