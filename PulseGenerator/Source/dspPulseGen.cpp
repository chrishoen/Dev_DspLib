// Generate sin wave

void gen11(PdwSequence* aS)
{
   for (int k = 0; k < aS->mNs; k++)
   {
         Pdw tPdw;
         tPdw.mToa       = k * aS->mTs;
         tPdw.mAmplitude = 1.0;

         if (k % aS->mNm == 0)
         {
            aS->putPdw(&tPdw);
         }
   }
}


   
