static const double cPI = 3.14159265358979323846264338327950288419716939937510;

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Generate sin wave

void gen1(Signal* aSS)
{
   for (int k = 0; k < aSS->mNs; k++)
   {
         double t = k * aSS->mTs;
         aSS->mX[k] = sin(2.0 * cPI * aSS->mFp * t);
   }
}

   
