// Read json file and initialize the cal table.

void MyCalTable::initialize()
{
   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Read from the json file into a table vector.

   mTable.reset();
   mTable.mTableX[0] = 1.0f; mTable.mTableY[0] = 2.0f;
   mTable.mTableX[1] = 2.0f; mTable.mTableY[1] = 4.0f;
   mTable.mTableX[2] = 3.0f; mTable.mTableY[2] = 6.0f;
   mTable.mTableX[3] = 4.0f; mTable.mTableY[3] = 8.0f;
   mTable.initialize();
}


